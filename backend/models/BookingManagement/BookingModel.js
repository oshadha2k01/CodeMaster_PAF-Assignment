const mongoose = require('mongoose');

const bookingSchema = new mongoose.Schema({
    movieName: String,
    movieDate: String,
    movieTime: String,
    seatNumbers: [String],
    name: String,
    email: String,
    phone: String,
    createdAt: { type: Date, default: Date.now }
});

module.exports = mongoose.model('Booking', bookingSchema);